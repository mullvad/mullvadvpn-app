import Foundation

struct FigmaVariables: Codable {
    struct Mode: Codable {
        let name: String
        let id: String
    }

    struct ColorValue: Codable {
        let name: String
        let value: String
        let `var`: String
        let rootAlias: String
    }

    let name: String
    let values: [Value]

    struct NumberValue: Codable {
        let name: String
        let value: String
        let `var`: String
        let rootAlias: String
    }

    struct Value: Codable {
        let mode: Mode
        let color: [ColorValue]
        let number: [NumberValue]
    }
}

func generateSwiftCode(from jsonString: String) {
    guard let jsonData = jsonString.data(using: .utf8) else {
        print("Invalid JSON string.")
        return
    }

    var output = ""

    do {
        let variableSets = try JSONDecoder().decode([FigmaVariables].self, from: jsonData)
        output += """
        // Generated by FigmaVariablesConverter
        // Do not edit directly
        // Generated on \(Date().formatted(date: .abbreviated, time: .omitted))
        // To update, export `platform/ios` and `primitives` from Figma using `variables2css` in `json` format,
        // replace the content of `variableJsonString` in `figmaVariablesConverter.swift` and run `swift figmaVariablesConverter.swift`\n
        """
        output += "import SwiftUI\n\n"
        output += "enum MullvadColor {\n"
        let colors = variableSets.flatMap { $0.values.flatMap { $0.color } }
        let numbers = variableSets.flatMap { $0.values.flatMap { $0.number } }
        for color in colors {
            let swiftVarName = color.name
                .replacingOccurrences(of: "[^a-zA-Z0-9]", with: " ", options: .regularExpression)
                .split(separator: " ")
                .enumerated()
                .map { $0.offset == 0 ? $0.element.lowercased() : $0.element.capitalized }
                .joined()
            let value = color.var.isEmpty ? "Color(\"\(color.value)\")" : color.var
                .replacingOccurrences(of: "[^a-zA-Z0-9]", with: " ", options: .regularExpression)
                .split(separator: " ")
                .enumerated()
                .map { $0.offset == 0 ? $0.element.lowercased() : $0.element.capitalized }
                .joined()

            output += "    static let \(swiftVarName) = \(value)\n"
        }
        output += "}\n\n"
        output += "enum MullvadUIMetrics {\n"
        for number in numbers {
            let swiftVarName = number.name
                .replacingOccurrences(of: "[^a-zA-Z0-9]", with: " ", options: .regularExpression)
                .split(separator: " ")
                .enumerated()
                .map { $0.offset == 0 ? $0.element.lowercased() : $0.element.capitalized }
                .joined()
            let value = number.var.isEmpty ? "CGFloat(\(number.value.replacingOccurrences(of: "px", with: "")))" : number.var
                .replacingOccurrences(of: "[^a-zA-Z0-9]", with: " ", options: .regularExpression)
                .split(separator: " ")
                .enumerated()
                .map { $0.offset == 0 ? $0.element.lowercased() : $0.element.capitalized }
                .joined()

            output += "    static let \(swiftVarName) = \(value)\n"
        }
        output += "}\n"

        let fileURL = URL(fileURLWithPath: "MullvadVPN/UI appearance/FigmaVariables.swift")
        try output.write(to: fileURL, atomically: true, encoding: .utf8)
        print("Swift code written to MullvadVPN/UI appearance/FigmaVariables.swift")
    } catch {
        print("Error parsing JSON: \(error)")
    }
}

