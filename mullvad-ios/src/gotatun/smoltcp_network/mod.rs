/// Configuration for creating a [`SmoltcpNetwork`].
pub struct SmoltcpNetworkConfig {
    /// Source IPv4 address for the virtual interface.
    pub ipv4_addr: Ipv4Addr,
    /// Optional source IPv6 address.
    pub ipv6_addr: Option<Ipv6Addr>,
    /// MTU for the virtual interface. Should be `tunnel_mtu - WIREGUARD_HEADER_SIZE`.
    pub mtu: u16,
}

/// A userspace IP networking stack. Owns a background poll loop task.
///
/// When dropped, the poll loop is aborted.
pub struct SmoltcpNetwork {
    /// Handle for creating sockets. Cheaply cloneable.
    pub handle: SmoltcpHandle,
    /// The [`IpRecv`] half — yields packets generated by smoltcp to GotaTun.
    pub ip_recv: SmoltcpIpRecv,
    /// The [`IpSend`] half — receives decrypted return traffic from GotaTun.
    pub ip_send: SmoltcpIpSend,
    poll_task: JoinHandle<()>,
}

impl SmoltcpNetwork {
    /// Create a new smoltcp-based network stack.
    ///
    /// Spawns a background tokio task that drives the smoltcp interface.
    pub fn new(config: SmoltcpNetworkConfig) -> Self {
        let (to_gotatun_tx, to_gotatun_rx) = mpsc::channel(PACKET_CHANNEL_CAPACITY);
        let (from_gotatun_tx, from_gotatun_rx) = mpsc::channel(PACKET_CHANNEL_CAPACITY);
        let (cmd_tx, cmd_rx) = mpsc::channel(CMD_CHANNEL_CAPACITY);
        let notify = Arc::new(Notify::new());

        let mtu_watcher = MtuWatcher::new(config.mtu);

        let ip_recv = SmoltcpIpRecv {
            rx: to_gotatun_rx,
            mtu: mtu_watcher,
        };

        let ip_send = SmoltcpIpSend {
            tx: from_gotatun_tx,
            notify: notify.clone(),
        };

        let handle = SmoltcpHandle {
            cmd_tx,
            notify: notify.clone(),
        };

        let poll_task = tokio::spawn(poll_loop(
            config,
            from_gotatun_rx,
            to_gotatun_tx,
            cmd_rx,
            notify,
        ));

        Self {
            handle,
            ip_recv,
            ip_send,
            poll_task,
        }
    }
}

impl SmoltcpNetwork {
    /// Decompose into constituent parts, aborting the poll loop task on drop
    /// of the returned guard.
    pub fn into_parts(
        self,
    ) -> (
        SmoltcpHandle,
        SmoltcpIpRecv,
        SmoltcpIpSend,
        SmoltcpNetworkGuard,
    ) {
        // Use ManuallyDrop to avoid triggering Drop while moving fields out
        let this = std::mem::ManuallyDrop::new(self);
        let handle = this.handle.clone();
        // Safety: we're consuming self, so these fields won't be accessed again
        let ip_recv = unsafe { std::ptr::read(&this.ip_recv) };
        let ip_send = unsafe { std::ptr::read(&this.ip_send) };
        let poll_task = unsafe { std::ptr::read(&this.poll_task) };
        (handle, ip_recv, ip_send, SmoltcpNetworkGuard { poll_task })
    }
}

/// Guard that aborts the poll loop task when dropped.
pub struct SmoltcpNetworkGuard {
    poll_task: JoinHandle<()>,
}

impl Drop for SmoltcpNetworkGuard {
    fn drop(&mut self) {
        self.poll_task.abort();
    }
}

impl Drop for SmoltcpNetwork {
    fn drop(&mut self) {
        self.poll_task.abort();
    }
}

/// A cloneable handle for creating sockets on a [`SmoltcpNetwork`].
#[derive(Clone)]
pub struct SmoltcpHandle {
    cmd_tx: mpsc::Sender<SocketCmd>,
    notify: Arc<Notify>,
}

impl SmoltcpHandle {
    /// Open a TCP connection to the given address through the smoltcp stack.
    ///
    /// The returned stream may be used immediately. Reads will pend until the
    /// TCP handshake completes and data arrives. Writes are buffered and
    /// flushed once the connection is established.
    pub async fn tcp_connect(&self, addr: SocketAddr) -> io::Result<SmoltcpTcpStream> {
        let (tx, rx) = oneshot::channel();
        self.cmd_tx
            .send(SocketCmd::TcpConnect { addr, response: tx })
            .await
            .map_err(|_| io::Error::new(io::ErrorKind::BrokenPipe, "poll loop closed"))?;
        self.notify.notify_one();
        rx.await
            .map_err(|_| io::Error::new(io::ErrorKind::BrokenPipe, "poll loop dropped"))?
    }

    /// Create an ICMP socket bound to the given identifier.
    ///
    /// Only ICMP echo replies matching this identifier will be delivered.
    pub async fn icmp_socket(&self, ident: u16) -> io::Result<SmoltcpIcmpSocket> {
        let (tx, rx) = oneshot::channel();
        self.cmd_tx
            .send(SocketCmd::CreateIcmpSocket {
                ident,
                response: tx,
            })
            .await
            .map_err(|_| io::Error::new(io::ErrorKind::BrokenPipe, "poll loop closed"))?;
        self.notify.notify_one();
        rx.await
            .map_err(|_| io::Error::new(io::ErrorKind::BrokenPipe, "poll loop dropped"))?
    }
}

