//! A userspace IP networking stack built on smoltcp, providing TCP and ICMP
//! sockets whose traffic is surfaced as gotatun [`IpSend`]/[`IpRecv`] streams.
//!
//! This is used on iOS where the tunnel process cannot bind sockets to the
//! tunnel device directly. Instead, smoltcp generates raw IP packets that are
//! fed into a GotaTun device via [`IpRecv`], and decrypted return traffic is
//! fed back via [`IpSend`].

mod device;

use bytes::BytesMut;
use device::SmoltcpDevice;
use gotatun::{
    packet::{Ip, Packet, PacketBufPool},
    tun::{IpRecv, IpSend, MtuWatcher},
};
use smoltcp::{
    iface::{Config as IfaceConfig, Interface, SocketHandle, SocketSet},
    socket::{icmp, tcp},
    time::Instant as SmoltcpInstant,
    wire::{IpAddress, IpCidr, IpEndpoint, IpListenEndpoint},
};
use std::{
    collections::VecDeque,
    io,
    net::{Ipv4Addr, Ipv6Addr, SocketAddr},
    pin::Pin,
    sync::Arc,
    task::{Context, Poll, ready},
    time::Instant as StdInstant,
};
use tokio::{
    io::{AsyncRead, AsyncWrite, ReadBuf},
    sync::{Notify, mpsc, oneshot},
    task::JoinHandle,
};

/// Channel capacity for packets between SmoltcpNetwork and GotaTun.
const PACKET_CHANNEL_CAPACITY: usize = 256;
/// Channel capacity for TCP data between socket handles and the poll loop.
const TCP_DATA_CHANNEL_CAPACITY: usize = 64;
/// Channel capacity for ICMP between socket handles and the poll loop.
const ICMP_CHANNEL_CAPACITY: usize = 16;
/// Channel capacity for socket commands.
const CMD_CHANNEL_CAPACITY: usize = 16;

/// Size of TCP socket receive and send buffers within smoltcp.
const TCP_BUFFER_SIZE: usize = 65535;
/// Number of ICMP packet metadata slots.
const ICMP_METADATA_SLOTS: usize = 8;
/// Size of ICMP packet buffer.
const ICMP_BUFFER_SIZE: usize = 2048;

